package ForLoop;

public class PrintTime {
    public static void main(String[] args) {


        for (int i=1; i<=12; i++) {
            System.out.println(i + " Hours");

            for (int m=1; m<=60; m++) {

                System.out.print(m + " ");
            }
            System.out.println();
        }


    }
}
